package a5;

public class School {
	private String name, loc, board;
	
	public String getSchoolName() {
		return this.name;
	}
	
	public String getLoc() {
		return this.loc;
	}
	
	public String getBoard() {
		return this.board;
	}
	
	public void setSchoolName(String name) {
		
		this.name = name;
	}
	
	public void setLoc(String loc) {
		this.loc = loc;
	}
	
	public void setBoard(String board) { 
		this.board = board;
	}
}
